const swiper = new Swiper('.swiper', {
  // Optional parameters
  loop: true,

  // If we need pagination
  pagination: {
    el: '.swiper-pagination',
    clickable: true,
  },
  
  spaceBetween: 30,



  centeredSlides: true,

  // Navigation arrows
  navigation: {
    nextEl: '.swiper-button-next',
    prevEl: '.swiper-button-prev',
  },

  breakpoints: {
    0: {
      slidesPerView: 1,   // mobile
    },
    768: {
      slidesPerView: 2,   // tablet
    },
    1280: {
      slidesPerView: 3,  // desktop
    }
  }
});